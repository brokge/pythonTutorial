#!/usr/bin/python

class MyNumbers:
    def __iter__(self):
        self.a = 1
        return self

    def __next__(self):
        # 限定 迭代器 在 大于 20的时候抛出异常
        if self.a < 20:
          x = self.a
          self.a += 1
          return x
        else:
            raise StopIteration

myclass = MyNumbers()
myiter = iter(myclass)

